											<p>Le 6 octobre 1973, le jour de la fête du Kippour, la plus importante du calendrier juif, l'Égypte et la Syrie lancent par surprise une attaque coordonnée contre Israël: l'Égypte attaque par le Sinaï, la Syrie par le plateau du Golan (le plateau du Golan contrôle le lac de Tibériade et toutes les ressources en eau du Nord d'Israël). L'armée israélienne est d'abord mise en difficulté, mais au bout de quelques jours repousse les assaillants grâce à un ravitaillement en munitions fourni par un pont aérien américain. L'armée égyptienne en progressant rapidement dans le Sinaï se retrouve coupée de son ravitaillement, Israël profite de ce répit pour reprendre l'initiative grâce au général Sharon qui fait couper les arrières de l'armée égyptienne en envoyant ses hommes de l'autre côté du canal. L'armée égyptienne est alors contrainte à la reddition. La Syrie poursuit la guerre avec l'espoir de récupérer le reste de Golan, mais sans résultat.
											L'ONU adopte le 22 octobre1973 la Résolution 338 (1973), qui réaffirme la validité de la résolution 242 (1967), adoptée pendant la guerre des Six Jours et appelle toutes les parties en conflit (l'Égypte, la Syrie, Israël, la Jordanie) à un cessez-le-feu immédiat et à des négociations en vue « d'instaurer une paix juste et durable au Moyen-Orient ».
											Les combats cessent le 25 octobre et le cessez-le-feu israélo-égyptien est signé le 28 octobre au « kilomètre 101 » (c'est-àdire à 101 kilomètres du Caire) par les généraux israélien Aharon Yariv et égyptien Abdel Ghani el-Gamasy. Sur le front syrien, les combats s'interrompent quand les Israéliens sont à une quarantaine de kilomètres de Damas.
											Cette guerre montre aussi aux Arabes que les Israéliens sont vulnérables et efface en quelque sorte l'humiliation de la guerre des Six jours, ce qui permet l'ouverture de négociations de paix.</p>
