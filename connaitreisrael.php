											<p>Elle se déroula du lundi 5 au samedi 10 juin 1967 et opposa Israël à l'Égypte, la Jordanie et la Syrie.
                                         Cette guerre fut déclenchée comme une « attaque préventive » d'Israël contre ses voisins arabes, à la suite du blocus du détroit de Tiran aux navires israéliens par l'Égypte le 23 mai 1967 (les Israéliens avaient préalablement annoncé qu'ils considéreraient cet acte comme un casus belli). Le soir de la première journée de guerre, la moitié de l'aviation arabe était détruite ; le soir du sixième jour, les armées égyptiennes, syriennes et jordaniennes étaient défaites. Les chars de l'armée israélienne bousculèrent leurs adversaires sur tous les fronts. En moins d'une semaine, l'État hébreu tripla son emprise territoriale : l'Égypte perdit la bande de Gaza et la péninsule du Sinaï, la Syrie fut amputée du plateau du Golan et la Jordanie de la Cisjordanie et de Jérusalem-Est.</p>
											<p>   Plus symbolique encore que la défaite arabe fut la prise de la vieille ville de Jérusalem. Israël considère alors cetteville comme sa capitale, sans la reconnaissance de la plus grande partie de la communauté internationale.
                                       Les résultats de cette guerre, épisode du conflit israélo-arabe, influencent encore aujourd'hui la géopolitique de la région. En particulier, certains territoires furent annexés ou sont toujours occupés par Israël, par exemple une partie de la Cisjordanie. </p> <br>
											<p> <h4>De 1967 à 1979 : les années de transformation</h4></p>
											<p> Après la guerre des Six jours, les Israéliens et leur gouvernement sont persuadés de leur supériorité face aux armées arabes. Cette certitude est renforcée par l'échec de la guerre d'usure lancée par les Égyptiens. C'est ainsi que le gouvernement israélien devient aveugle aux préparatifs militaires de Sadate. La guerre du Kippour, lancée le 6 octobre 1973 par les Égyptiens surprend donc totalement Israël.</p>

