											<p>  En  février 1947,   le  gouvernement britannique remet le mandat qu'il détenait depuis 1920 sur la Palestine aux Nations unies.Le 29 novembre 1947, l'Assemblée générale des Nations unies adopte la résolution 181 qui prévoit le partage de la Palestine en un État juif et un État arabe. Le nouveau Yichouv et les communautés juives sionistes accueillent favorablement ce vote mais les Arabes palestiniens et l'ensemble des pays arabes qui militaient pour la constitution d'un État arabe sur toute la Palestine rejettent la résolution. Le lendemain du vote, la guerre civile éclate entre les communautés juive et arabe palestiniennes.
