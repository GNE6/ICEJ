											<p>Le 9 novembre 1977, le président égyptien Anouar el-Sadate annonce qu'il est prêt à se rendre en visite officielle à Jérusalem pour convaincre les Israéliens de sa volonté de paix. Après quelques jours de tergiversations, le premier ministre Menahem Begin lui fait parvenir une invitation.
											Sadate effectue un séjour du 19 au 21 novembre en Israël durant lequel il se rend à Yad Vashem, rencontre Golda Meir et prononce un discours à la Knesset. Le discours de Sadate et celui de Begin qui lui répond sont d'une « intransigeance » totale mais brisent « l'impasse » qui existait jusqu'alors. Il faut toutefois l'implication du président américain Jimmy Carter pour relancer les négociations. En septembre 1978, le président américain Jimmy Carter invite le président Sadate et le Premier ministre israélien Menahem Begin à Camp David pour négocier les accords de paix. Le 17 septembre 1978, Sadate et Begin signent deux accordscadres à Camp David, définissant les bases de négociations entre l'Égypte et Israë.
											Le 26 mars 1979, Anouar el-Sadate et Menahem Begin signent les accords de Camp David qui prévoient le retrait israélien du Sinaï et la reconnaissance de l'État d'Israël par l'Égypte. Conformément au traité, Israël se retire du Sinaï en avril 1982. L'évacuation de l'implantation de Yamit ne va pas sans quelque résistance de ses habitants.
											Pour leur implication dans ces négociations, Anouar el-Sadate et Menahem Begin reçoivent le prix Nobel de la paix en 1978.</p>
