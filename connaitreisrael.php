											<h4> De 1948 à 1967 :</h4> les années de formation.La guerre gagnée, la priorité pour le nouvel état est de se doter d'institutions. Les premières élections législatives ont lieu en janvier 1949 et donnent la majorité relative au Mapai (gauche) qui peut former un gouvernement dirigé par David Ben Gourion.</p>  
											<p>La guerre gagnée, la priorité pour le nouvel état est de se doter d'institutions. Les premières élections législatives ont lieu en janvier 1949 et donnent la majorité relative au Mapai (gauche) qui peut former un gouvernement dirigé par David Ben Gourion. Toutefois, le jeune état ne réussit pas à s'insérer dans la région. Les accrochages et les actes de sabotage à l'intérieur d'Israël se comptent par milliers et plus de 400 Israéliens sont tués de 1951 à 1956. Ce harcèlement permanent, la montée en puissance de Gamal Abdel Nasser, ses préparatifs militaires et le blocus du détroit de Tiran qu'il instaure, débouchent sur la campagne du Sinaï et la crise de Suez.</p> 
											   
